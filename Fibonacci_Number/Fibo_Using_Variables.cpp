/*-----Thuật toán tìm số fibonacci sử dụng các biến để tính toán-----*/
/*-----Search Fibonacci number algorithms using Variables-----*/

#include<bits/stdc++.h>

int Fibonacci(int n){
    int f1 = 1, f2 = 1, f3 = 0;
    for(int i = 2; i <= n; i++){
        f3 = f2 + f1;
        f1 = f2;
        f2 = f3;
    }
    return f3;
}

int main(){
    for(int i = 2; i < 20; i++) std::cout << Fibonacci(i) << " ";
}