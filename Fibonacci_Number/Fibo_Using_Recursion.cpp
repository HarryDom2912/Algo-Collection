/*-----Thuật toán tìm số fibonacci sử dụng đệ quy-----*/
/*-----Search Fibonacci number algorithms using Recursion-----*/

#include<bits/stdc++.h>

int Fibonacci(int n){
    if(n == 0 || n == 1) return 1;
    return Fibonacci(n -1) + Fibonacci(n - 2);
}

int main(){
    for(int i = 2; i < 20; i++){
        std::cout << Fibonacci(i) << " ";
    }
}