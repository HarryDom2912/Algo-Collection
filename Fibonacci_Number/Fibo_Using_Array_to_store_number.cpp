/*-----Thuật toán tìm số fibonacci sử dụng mảng để lưu kết quả-----*/
/*-----Search Fibonacci number algorithms using Array to store number-----*/

#include<bits/stdc++.h>

int arr[50] = {0};

int Fibonacci(int n){
    arr[0] = arr[1] = 1;
    for(int i = 2; i <= n; i++) arr[i] = arr[i -1] + arr[i - 2];
    return arr[n];
}

int main(){
    for(int i = 2; i < 20; i++){
        std::cout << Fibonacci(i) << " ";
    }
}